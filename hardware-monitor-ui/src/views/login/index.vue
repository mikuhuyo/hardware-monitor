<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" autocomplete="on"
      label-position="left">
      <div class="title-container">
        <h3 class="title">
          <img src="../../assets/loginIcon.png" alt="">
        </h3>
      </div>

      <el-form-item prop="username">
        <el-input ref="username" v-model="loginForm.username" name="username" type="text" autocomplete="on"
          placeholder="请输入用户名" />
      </el-form-item>

      <el-form-item prop="password">
        <el-input :key="passwordType" ref="password" v-model="loginForm.password" :type="passwordType"
          placeholder="请输入密码" name="password" autocomplete="on" @keyup.enter.native="handleLogin" />
        <span class="show-pwd" @click="showPwd">
        </span>
      </el-form-item>

      <el-button :loading="loading" type="primary" class="submit" @click.native.prevent="handleLogin">
        登录
      </el-button>
    </el-form>
  </div>
</template>

<script lang="ts">
  import {
    Component,
    Vue,
    Watch
  } from "vue-property-decorator";
  import {
    Route
  } from "vue-router";
  import {
    Dictionary
  } from "vue-router/types/router";
  import {
    Form as ElForm,
    Input
  } from "element-ui";
  import {
    UserModule
  } from "@/store/modules/user";
  import {
    isValidUsername
  } from "@/utils/validate";

  Component({
    name: "Login"
  })

  export default class extends Vue {
    private validateUsername = (rule: any, value: string, callback: Function) => {
      if (!isValidUsername(value)) {
        callback(new Error("请输入正确的账号"));
      } else {
        callback();
      }
    };
    private validatePassword = (rule: any, value: string, callback: Function) => {
      if (value.length < 5) {
        callback(new Error("请输入正确的密码"));
      } else {
        callback();
      }
    };
    private loginForm = {
      username: "mikuhuyo",
      password: "ih3MA1mi+UW6/IKqQXk0lc7J8Lov5UnfMnWaNIYtypGGYYP8B+MPuuiU0gVZjbaC3IfZbLkJdfdBZwufF+2vxKihDu+NUku7WrLoKOG/0PvmlXPLr7zfZqCb6nJHj9HmGCvX+UZfwFYvg3UeBqAVlnhfoZ4begUhoaJKx8J/umY="
    };
    private loginRules = {
      username: [{
        validator: this.validateUsername,
        trigger: "blur"
      }],
      password: [{
        validator: this.validatePassword,
        trigger: "blur"
      }]
    };
    private passwordType = "password";
    private loading = false;
    private showDialog = false;
    private redirect ? : string;
    private otherQuery: Dictionary < string > = {};

    @Watch("$route", {
      immediate: true
    })
    private onRouteChange(route: Route) {
      const query = route.query as Dictionary < string > ;
      if (query) {
        this.redirect = query.redirect;
        this.otherQuery = this.getOtherQuery(query);
      }
    }

    mounted() {
      if (this.loginForm.username === "") {
        (this.$refs.username as Input).focus();
      } else if (this.loginForm.password === "") {
        (this.$refs.password as Input).focus();
      }
    }

    private showPwd() {
      if (this.passwordType === "password") {
        this.passwordType = "";
      } else {
        this.passwordType = "password";
      }
      this.$nextTick(() => {
        (this.$refs.password as Input).focus();
      });
    }

    private handleLogin() {
      (this.$refs.loginForm as ElForm).validate(async (valid: boolean) => {
        if (valid) {
          this.loading = true;
          await UserModule.Login(this.loginForm).then((res: any) => {
            if (res.success) {
              this.$router.push({
                path: this.redirect || "/",
                query: this.otherQuery
              });
              // Just to simulate the time of the request
              setTimeout(() => {
                this.loading = false;
              }, 0.5 * 1000);
            } else {
              this.$message.error('用户名或密码错误')
              this.loading = false;
            }
          })

        } else {
          return false;
        }
      });
    }

    private getOtherQuery(query: Dictionary < string > ) {
      return Object.keys(query).reduce((acc, cur) => {
          if (cur !== "redirect") {
            acc[cur] = query[cur];
          }
          return acc;
        }, {} as Dictionary < string > );
    }
  }
</script>

